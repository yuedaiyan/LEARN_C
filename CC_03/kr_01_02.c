#include <stdio.h>
int main() {
    // 1. 使用双引号初始化字符数组
    // "Hi" 占用 3 个字节：'H', 'i', 以及自动添加的结束符 '\0'
    char x[3] = "Hi"; 

    // 2. 使用单引号逐个初始化字符数组
    // 这种写法手动指定了前两个元素，剩余的会被自动补 0 (\0)
    char y[3] = { 'H', 'i' }; 

    // 打印结果
    printf("x %s\n", x);    // %s 用于打印字符串，直到遇到 \0
    printf("y %s\n", y);    // 结果同上
    printf("%s\n", "Hi");   // 直接打印字符串常量
    printf("%c%c\n", 'H', 'i'); // %c 用于打印单个字符
}